let date = new Date();
console.log("year: " + date.getFullYear()); // Returns the year (e.g., 2024)
console.log("month: " + date.getMonth());    // Returns the month (0–11, where January is 0)
console.log("day: " + date.getDate());     // Returns the day of the month (1–31)
console.log("hour: " + date.getHours());    // Returns the hour (0–23)
console.log("minute: " + date.getMinutes());  // Returns the minutes (0–59)
console.log("second: " + date.getSeconds());  // Returns the seconds (0–59)

